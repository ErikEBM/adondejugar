class Mate {

    
    constructor(){
       
    }

    suma(valor1, valor2){
        return valor1+valor2;

    }

    resta(valor1, valor2){
        return valor1-valor2;
    }

    producto(valor1, valor2){
        return valor1*valor2;
    }

    division(valor1, valor2){
        return valor1/valor2;
    }

    residuo(valor1, valor2){
        return valor1%valor2;
    }
}

export var matematica = new Mate()